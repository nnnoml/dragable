十年前，我刚迈入社会不久，月薪只有区区三百元。我在广州工作两年以后，我的收入翻了十倍。2003年来的广州，在2005年的时候就到三千。我在300块钱时还能够省钱下来，住在宿舍里面、吃食堂，几乎就没怎么花钱，但是当我的月收入达到三千的时候，我偶尔还要向别人借钱。这是典型的“月光族”了吧？我发现问题不对了。当时有一本书，点醒了我——《富爸爸穷爸爸》，他里面提出一个很重要的观点，就是财务自由这个观点。它告诉我，财务自由跟金钱的绝对数量并没有关系，它其实就是一个简单的等式：财务自由=被动收入≥日常开支就是这么简单。 我恍然大悟了，原来财务自由并不难，只要平衡就好。就是说，我可能暂时赚钱没那么多，我的被动收入没那么多，那我能不能稍微省一点呢？实际上你只要保持这个平衡。许多人对于财富自由的理解，是另外一个等式：财务自由＝超级富豪（错！！！）就好像他们同时也会认为“理财＝投资”一样。回到刚才的等式，日常开支应该不用解释了，那什么是被动收入呢？比方说，你有一套住房用于出租，每个月收到的租金就是被动收入；或是你买了一些基金或是理财产品，每年都会收到的分红，这些同样也是被动收入。简单理解就是：只要不是你通过主动劳动赚到的钱，都可以算作是被动收入。你只要让这个等式成立了，你就实现了财务自由。我举一个例子来说明：广州2014年的平均月薪是6911元，扣除五险一金和个税以后，仅剩5700元左右。如果是夫妻两个人都能拿到平均月薪，那就是5700×2＝11400元，我相信还有很多家庭的收入是达不到11400元的。如果他们拿出每月收入的80%用于生活开支，那么家庭年开支就是11400×12×80%≈11万元。接下来再反向推算，如果这个家庭拥有110万元的投资资产，年收益率能够达到10%，那么就实现财务自由了。如果收益率达不到这么高，只能做到6%的话，那么这个家庭只要拥有183万的投资资产也同样实现了财务自由。也许有人会说：“才183万就能实现财务自由了？很多人一套房产就超过200万了！”这就对了，很多人的200万在睡大觉，他们是拥有价值200万的自住房产，不能产生现金流；而我的每一分钱都在源源不断的帮我赚钱，这就是我能够更早实现财务自由的原因。因此，实现财务自由并非遥不可及，但也不是太容易。它需要一定规模的可投资资产以及合理的收益率，最重要的是，还要能够控制好自己的家庭开支。要知道，很多身家超过200万的人，家庭年开支肯定是会超过11万的，这也是他们永远也无法实现财务自由的原因。拥有越多，欲望就越大，财务自由就渐行渐远了。实现财务自由以后就意味着退休不干活了吗？要知道，富爸爸的作者罗伯特•清崎正是在实现财务自由以后，才写出了《富爸爸穷爸爸》这本畅销书，最终让他进入了真正的富豪行列。





所以，财务自由也并不意味着终结，这可能正是富豪之路的开始。财富从0到1，靠的不是理财而是事业再回到我的故事。“财务自由”之前是财务平衡，要达到财务平衡，我必须在两端同时发力，首先，我要积极赚钱。这个“赚钱”指的不是投资赚钱。实际上，作为一名打工者，我们还是要在升职加薪方面动脑筋，增加收入，同时控制欲望。我所说的“开源”跟别人说的不同。普通的理财师认为“开源”就是把你的本金拿去投资，接着有现金流入。我不这么认为，对于大多数理财小白来说，他们没有多少本金。对于一个大水池来说，你那个在滴水，太阳晒的可能比你滴进来的还要多，这个就不叫开源了。升职加薪是最快的开源，因为你还在打工。我也是走过弯路的。2005年我开始学习理财。当时我刚刚迈入社会不久，房产投资对我来说门槛太高，股票和基金相对来说门槛不高，资产变现也更容易，就成了我优选考虑的投资工具。那时我年轻气盛，觉得基金涨得太慢，感觉股票会更符合我的需求，于是主攻股票投资。2006年初，恰逢中国最大的一波牛市行情启动，我的幸运时刻来临了。那时我的本金不多，胆子也不够大，通过学习和高人指点，自以为积累了不少股票基础知识和实战经验，对自己的投资策略信心满满。好景不长，到了2007年底，股市急转直下。刚开始，我的师傅们还是斗志昂扬，说马上就会收复失地，再创历史新高，后来经过几轮大跌后，在证券市场就很少见到他们的身影了。幸好那时我胆子比较小，早早清仓，还保存了一点牛市的硕果。经过那一轮牛市熊市的历练，我越发觉得，散户直接买卖股票是一件费力不讨好的事情，我开始转而研究基金，特别是指数基金。因为惯性思维，刚开始研究基金时，我仍然会把基金当成股票来操作，买卖次数非常频繁，白白浪费了不少申购费和赎回费。直到2008年底，我才真正开始将基金定投作为主要投资方式之一，用基金定投来规划我的养老金。当然，坚持定投也是很困难的事情，需要战胜人性。说实话，我也中断过几次。直到2011年底，我才真正做到每月坚持基金定投。通过这一波三折的投资经历，我清楚认识到一点：专业化投资需要花费大量时间精力，还需要一定天赋。始终立于不败之地的投资大师屈指可数，很多所谓的投资高手都只是昙花一现，有人甚至因投资倾家荡产。这都是试图通过投资“赚大钱”、“赚快钱”的下场。





